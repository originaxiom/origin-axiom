import mpmath as mp
mp.mp.dps = 45

m_A   = mp.mpf('0.646131894438901028187273021447612788144483682')
Vol   = mp.mpf('2.029883212819307250042405108549040571883')
V2pi  = Vol/(2*mp.pi)
Lp0   = mp.mpf('0.251330433713252231374872566669336294636860391')
LE2   = mp.mpf('0.661475187921069742727520633979626889791045796')
pi    = mp.pi

print("=== THE THREE NUMBERS ===")
print("m(A_41)      =", mp.nstr(m_A,40))
print("Vol/(2pi)    =", mp.nstr(V2pi,40))
print("L'(E15,0)    =", mp.nstr(Lp0,40))
print("L(E15,2)     =", mp.nstr(LE2,40))
print()
print("=== LEG 1: m vs Vol ===")
print("m /(Vol/2pi) =", mp.nstr(m_A/V2pi,35))
print("m*pi/Vol     =", mp.nstr(m_A*pi/Vol,35))
print("=> m(A_41) = Vol/pi (ratio 2 vs Vol/2pi):", mp.nstr(m_A - Vol/pi, 5))
print()
print("=== LEG 2: m vs L'(E15,0) ===")
print("m / L'(E15,0)      =", mp.nstr(m_A/Lp0,35))
print("(Vol/2pi)/L'(E15,0)=", mp.nstr(V2pi/Lp0,35))
print("L'(E15,0)/(Vol/2pi)=", mp.nstr(Lp0/V2pi,35))
print("m / L(E15,2)       =", mp.nstr(m_A/LE2,35))
print("L'(E15,0)/L(E15,2) =", mp.nstr(Lp0/LE2,35), " (should be N/4pi^2=",mp.nstr(15/(4*pi**2),20),")")
print()
print("=== PSLQ integer-relation searches ===")
def pslq_try(label, vec):
    r = mp.pslq(vec, maxcoeff=10**6, maxsteps=10**5)
    print(label, "->", r)
pslq_try("[m, V2pi]         ", [m_A, V2pi])
pslq_try("[m, Lp0]          ", [m_A, Lp0])
pslq_try("[V2pi, Lp0]       ", [V2pi, Lp0])
pslq_try("[m, Lp0, V2pi]    ", [m_A, Lp0, V2pi])
pslq_try("[m, Lp0, pi]      ", [m_A, Lp0, pi])
pslq_try("[m, LE2]          ", [m_A, LE2])
pslq_try("[m, LE2, pi]      ", [m_A, LE2, pi])
