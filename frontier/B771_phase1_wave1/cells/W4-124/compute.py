def in_Qsqrt2(v, tol):
    v=mp.mpc(v)
    if abs(mp.im(v))>tol: return False
    if abs(mp.re(v))<tol: return True          # exact 0 is in Q(sqrt2)
    rel=mp.pslq([mp.mpf(1), mp.sqrt(2), mp.re(v)], tol=tol, maxcoeff=10**8)
    return rel is not None and rel[2]!=0
def is_rational(v, tol):
    v=mp.mpc(v)
    if abs(mp.im(v))>tol: return False
    if abs(mp.re(v))<tol: return True          # exact 0 is rational
    rel=mp.pslq([mp.mpf(1), mp.re(v)], tol=tol, maxcoeff=10**8)
    return rel is not None and rel[1]!=0

