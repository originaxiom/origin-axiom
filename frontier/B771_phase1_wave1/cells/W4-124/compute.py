def conjugacy_classes(elts, mul, ident):
    els = list(elts); invs = {g: inverse(g, mul, ident) for g in els}
    seen = set(); classes = []
    for g in els:
        if g in seen: continue
        orbit = set(mul(mul(x, g), invs[x]) for x in els)
        seen |= orbit; classes.append(sorted(orbit, key=lambda t: str(t)))
    return classes

