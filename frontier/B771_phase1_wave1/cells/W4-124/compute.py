def quat_mul(P, Q):
    w1,x1,y1,z1 = P; w2,x2,y2,z2 = Q
    w = qsub(qsub(qsub(qmul_f(w1,w2),qmul_f(x1,x2)),qmul_f(y1,y2)),qmul_f(z1,z2))
    x = qadd(qadd(qadd(qmul_f(w1,x2),qmul_f(x1,w2)),qmul_f(y1,z2)),qneg(qmul_f(z1,y2)))
    y = qadd(qadd(qsub(qmul_f(w1,y2),qmul_f(x1,z2)),qmul_f(y1,w2)),qmul_f(z1,x2))
    z = qadd(qsub(qadd(qmul_f(w1,z2),qmul_f(x1,y2)),qmul_f(y1,x2)),qmul_f(z1,w2))
    return (w,x,y,z)
