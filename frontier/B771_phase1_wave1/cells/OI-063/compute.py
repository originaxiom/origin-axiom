def lcm(a, b):
    return a * b // gcd(a, b)

# ---------------------------------------------------------------- core: words and matrices
def matmul2(A, B):
    return [[A[0][0]*B[0][0]+A[0][1]*B[1][0], A[0][0]*B[0][1]+A[0][1]*B[1][1]],
            [A[1][0]*B[0][0]+A[1][1]*B[1][0], A[1][0]*B[0][1]+A[1][1]*B[1][1]]]

S2 = [[0, -1], [1, 0]]

def factor_ST(M):
    """B216 factorization gamma = prod T^{q_i}S * T^m * (S^2 if e=-1). Returns ops,(e,m)."""
    M = [[int(M[0][0]), int(M[0][1])], [int(M[1][0]), int(M[1][1])]]
    ops = []
    while M[1][0] != 0:
        a, c = M[0][0], M[1][0]
        q = int(round(a / c))
        M = [[a - q * M[1][0], M[0][1] - q * M[1][1]], [M[1][0], M[1][1]]]
        M = [[M[1][0], M[1][1]], [-M[0][0], -M[0][1]]]
        ops.append(q)
    return ops, (M[0][0], M[0][1])

def cyclic_word(M):
    """cyclic q-word: W := prod_i T^{q_i}S is T^mm-conjugate of +-M. Returns (qs, W)."""
    ops, (e, m) = factor_ST(M)
    qs = list(ops)
    mm = m if e == 1 else -m
    if not qs:
        raise ValueError("triangular gamma (not hyperbolic)")
    qs[0] += mm
    W = [[1, 0], [0, 1]]
    for q in qs:
        W = matmul2(W, matmul2([[1, q], [0, 1]], S2))
    # sanity: W is conjugate to +-M => |tr W| = |tr M|
    assert abs(W[0][0] + W[1][1]) == abs(M[0][0] + M[1][1]), "word trace mismatch"
    return qs, W

def content(M):
    a, b, c, d = M[0][0], M[0][1], M[1][0], M[1][1]
    return reduce(gcd, [abs(b), abs(c), abs(a - d)])

def law_period(M):
    t = abs(M[0][0] + M[1][1])
    return lcm(t - 2, t + 2) // content(M)

def Qgamma_val(M, v1, v2):
    """the trace form Q_gamma(v) = c v1^2 + (d-a) v1 v2 - b v2^2 (content = B219 content)."""
    a, b, c, d = M[0][0], M[0][1], M[1][0], M[1][1]
    return c * v1 * v1 + (d - a) * v1 * v2 - b * v2 * v2

# ---------------------------------------------------------------- ground truth |Z| (B216 rho)
def rho_trace_abs(M, n):
    k = n - 2
    j = np.arange(k + 1)
    Smat = np.sqrt(2.0 / n) * np.sin(np.pi * np.outer(j + 1, j + 1) / n)
    Td = np.exp(1j * np.pi * ((j + 1) ** 2 / (2.0 * n)))
    ops, (e, m) = factor_ST(M)
    rho = np.eye(k + 1, dtype=complex)
    for q in ops:
        rho = (rho * (Td ** q)) @ Smat        # rho @ diag(Td^q) @ S, vectorized
    rho = rho * (Td ** m) if e == 1 else (rho @ (Smat @ Smat)) * (Td ** (-m))
    return abs(np.trace(rho))

# ---------------------------------------------------------------- sector forms + exact frequencies
def build_Q(qs, eps):
    r = len(qs)
    Q = [[0] * r for _ in range(r)]
    for i in range(r):
        Q[i][i] = qs[i]
    for i in range(r):
        j = (i + 1) % r
        if i == j:
            Q[i][i] += 2 * eps[i]
        else:
            Q[i][j] += eps[i]
            Q[j][i] += eps[i]
    return Q

def det_int(Q):
    return int(round(float(sp.Matrix(Q).det())))

def quotient_reps(Q):
    """representatives of Z^r/QZ^r via SNF with left transform."""
    M = sp.Matrix(Q)
    r = M.shape[0]
    A = sp.eye(r)
    Mw = M[:, :]
    t0 = 0
    while t0 < r:
        while True:
            piv, bestabs = None, None
            for i in range(t0, r):
                for j in range(t0, r):
                    v = Mw[i, j]
                    if v != 0 and (bestabs is None or abs(v) < bestabs):
                        bestabs, piv = abs(v), (i, j)
            if piv is None:
                break
            i0, j0 = piv
            if i0 != t0:
                Mw.row_swap(t0, i0); A.row_swap(t0, i0)
            if j0 != t0:
                Mw.col_swap(t0, j0)
            done = True
            for i in range(t0 + 1, r):
                qq = Mw[i, t0] // Mw[t0, t0]
                if qq != 0:
                    Mw[i, :] = Mw[i, :] - qq * Mw[t0, :]
                    A[i, :] = A[i, :] - qq * A[t0, :]
                if Mw[i, t0] != 0:
                    done = False
            for j in range(t0 + 1, r):
                qq = Mw[t0, j] // Mw[t0, t0]
                if qq != 0:
                    Mw[:, j] = Mw[:, j] - qq * Mw[:, t0]
                if Mw[t0, j] != 0:
                    done = False
            if done:
                break
        t0 += 1
    s = [int(Mw[i, i]) for i in range(r)]
    Ainv = A.inv()
    assert all(x == int(x) for x in Ainv)
    Ainv = [[int(Ainv[i, j]) for j in range(r)] for i in range(r)]
    reps = []
    ranges = [range(abs(x)) if x != 0 else [0] for x in s]
    for w in itertools.product(*ranges):
        reps.append([sum(Ainv[i][j] * w[j] for j in range(r)) for i in range(r)])
    return reps

def sector_freqs(Q):
    """exact multiset {y^T Q^{-1} y mod 1} over Z^r/QZ^r, as dict Fraction->count."""
    r = len(Q)
    Qs = sp.Matrix(Q)
    D = int(Qs.det())
    adj = Qs.adjugate()
    adj = [[int(adj[i, j]) for j in range(r)] for i in range(r)]
    counts = {}
    for y in quotient_reps(Q):
        num = sum(y[i] * adj[i][j] * y[j] for i in range(r) for j in range(r))
        f = Fraction(num, D) % 1
        counts[f] = counts.get(f, 0) + 1
    return counts

def signature(Q):
    ev = np.linalg.eigvalsh(np.array(Q, dtype=float))
    assert np.min(np.abs(ev)) > 1e-9
    return int(np.sum(ev > 0) - np.sum(ev < 0))

