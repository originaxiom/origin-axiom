# ---------------------------------------------------------------- sweep construction
def trace_classes(t, span=6):
    out = []
    for a in range(t - span, t + span + 1):
        P = a * (t - a) - 1
        if P == 0:
            continue
        for b in range(-abs(P), abs(P) + 1):
            if b == 0 or P % b != 0:
                continue
            c = P // b
            if a * (t - a) - b * c == 1:
                out.append([[a, b], [c, t - a]])
    return out

def build_sweep(tmax=26, reps_per_bucket=3):
    sweep = []
    buckets = {}
    for t in range(3, tmax + 1):
        for M in trace_classes(t):
            key = (t, content(M))
            if buckets.get(key, 0) < reps_per_bucket:
                buckets[key] = buckets.get(key, 0) + 1
                sweep.append(M)
    for M in ([[13, -8], [-8, 5]], [[17, -4], [-4, 1]],          # B216 GAMMA_A/B, f=8
              [[41, -64], [-16, 25]], [[41, -128], [-8, 25]]):   # B219 f=16 content 16/8
        if M not in sweep:
            sweep.append(M)
    return sweep

# ---------------------------------------------------------------- P4: B219 robust period detector
def minimal_period_measured(M, cap, k0=10, W=48, tol=2e-3):
    need = cap + W + 6
    v = [rho_trace_abs(M, n) for n in range(k0, k0 + need + 1)]
    for P in range(1, cap + 1):
        if all(abs(v[i] - v[i + P]) < tol for i in range(W)):
            return P
    return None

