def lemma_E1(rmax=7, nrand=6):
    """det Q_eps = tr W - (-1)^r 2 prod(eps), symbolic over Z[q_1..q_r] for all r<=rmax."""
    for r in range(1, rmax + 1):
        q = sp.symbols(f'q1:{r + 1}')
        pats = [[-1] * r, [-1] * (r - 1) + [1]]
        for _ in range(2):
            pats.append([random.choice([1, -1]) for _ in range(r)])
        for eps in pats:
            Q = sp.zeros(r, r)
            for i in range(r):
                Q[i, i] = q[i]
            for i in range(r):
                j = (i + 1) % r
                if i == j:
                    Q[i, i] += 2 * eps[i]
                else:
                    Q[i, j] += eps[i]
                    Q[j, i] += eps[i]
            Wm = sp.eye(2)
            for i in range(r):
                Wm = Wm * sp.Matrix([[q[i], -1], [1, 0]])
            lhs = sp.expand(Q.det())
            rhs = sp.expand(sp.trace(Wm) - (-1) ** r * 2 * sp.prod(eps))
            if sp.simplify(lhs - rhs) != 0:
                return False, (r, eps)
    # random large-r integer checks
    for _ in range(nrand):
        r = random.randint(8, 12)
        qs = [random.randint(-6, 6) for _ in range(r)]
        eps = [random.choice([1, -1]) for _ in range(r)]
        Q = build_Q(qs, eps)
        Wm = [[1, 0], [0, 1]]
        for qq in qs:
            Wm = matmul2(Wm, [[qq, -1], [1, 0]])
        if det_int(Q) != (Wm[0][0] + Wm[1][1]) - (-1) ** r * 2 * int(np.prod(eps)):
            return False, ('rand', r, qs, eps)
    return True, None

def lemma_E2():
    """Q_gamma(v+(gamma-sI)z) == Q_gamma(v) mod (t-s*2), s=+-1: polynomial identity.
    With d=t-a, c=(a d-1)/b: the difference, evaluated at t=2s, vanishes identically."""
    a, b, tsym, v1, v2, z1, z2 = sp.symbols('a b t v1 v2 z1 z2')
    d = tsym - a
    c = (a * d - 1) / b
    for s in (1, -1):
        M11, M12 = a - s, b
        M21, M22 = c, d - s
        w1 = v1 + M11 * z1 + M12 * z2
        w2 = v2 + M21 * z1 + M22 * z2
        Qv = c * v1 ** 2 + (d - a) * v1 * v2 - b * v2 ** 2
        Qw = c * w1 ** 2 + (d - a) * w1 * w2 - b * w2 ** 2
        diff_at = sp.simplify((Qw - Qv).subs(tsym, 2 * s))
        if diff_at != 0:
            return False, s
        # divisibility by (t-2s) in Q(a,b)[t,...]: remainder at t=2s vanishing suffices (monic in t)
    return True, None

def lemma_E3(rmax=6):
    """Step-5 value identity, symbolic over Q(q_1..q_r)[y]: for the cyclic sector form Q
    (corner eps_r = +-1, chain couplings -1), x = Q^{-1} y gives
      x^T Q x  =  B_W(u)/det(Q)  +  (polynomial with INTEGER coefficients in y, q),
    where u = integer-linear in y and B_W is an integer quadratic form built from W = prod T^{q_i}S
    (the W-adjugate boundary form).  We verify the *decisive* consequence symbolically:
      det(Q) * x^T Q x  is an integer-coefficient polynomial in (q, y)   [no denominators], and
      x^T Q x mod 1 depends on y only through u mod (W-+I)Z^2  -- via E2 + the exact multiset
      match (P2).  Here we verify the denominator-clearing identity det(Q)*x^T Q x in Z[q,y]."""
    for r in range(2, rmax + 1):
        q = sp.symbols(f'q1:{r + 1}')
        y = sp.symbols(f'y1:{r + 1}')
        for corner in (-1, 1):
            eps = [-1] * (r - 1) + [corner]
            Q = sp.zeros(r, r)
            for i in range(r):
                Q[i, i] = q[i]
            for i in range(r):
                j = (i + 1) % r
                if i == j:
                    Q[i, i] += 2 * eps[i]
                else:
                    Q[i, j] += eps[i]
                    Q[j, i] += eps[i]
            yv = sp.Matrix(y)
            D = Q.det()
            adjQ = Q.adjugate()
            num = sp.expand((yv.T * adjQ * yv)[0, 0])   # = det * x^T Q x
            # integer-coefficient polynomial in Z[q,y]?
            poly = sp.Poly(num, *q, *y)
            if not all(co == int(co) for co in poly.coeffs()):
                return False, (r, corner)
    return True, None

