# ----------------------------------------------------------------------------
# Exact arithmetic in Q(sqrt(D)), D > 0 squarefree (represented p + q*sqrt(D))
# ----------------------------------------------------------------------------

class QF:
    __slots__ = ("p", "q", "D")

    def __init__(self, p, q, D):
        self.p = Fraction(p)
        self.q = Fraction(q)
        self.D = int(D)

    def _chk(self, o):
        assert isinstance(o, QF) and o.D == self.D, "field mismatch"

    def __add__(self, o):
        self._chk(o)
        return QF(self.p + o.p, self.q + o.q, self.D)

    def __sub__(self, o):
        self._chk(o)
        return QF(self.p - o.p, self.q - o.q, self.D)

    def __neg__(self):
        return QF(-self.p, -self.q, self.D)

    def __mul__(self, o):
        self._chk(o)
        return QF(self.p * o.p + self.q * o.q * self.D,
                  self.p * o.q + self.q * o.p, self.D)

    def inv(self):
        n = self.p * self.p - self.q * self.q * self.D  # field norm (nonzero unless 0)
        assert n != 0, "division by zero in Q(sqrt(D))"
        return QF(self.p / n, -self.q / n, self.D)

    def __truediv__(self, o):
        self._chk(o)
        return self * o.inv()

    def is_zero(self):
        return self.p == 0 and self.q == 0

    def __eq__(self, o):
        return isinstance(o, QF) and o.D == self.D and self.p == o.p and self.q == o.q

    def sign(self):
        p, q, D = self.p, self.q, self.D
        if p == 0 and q == 0:
            return 0
        if p >= 0 and q >= 0:
            return 1
        if p <= 0 and q <= 0:
            return -1
        t = p * p - q * q * D  # compare magnitudes exactly
        assert t != 0, "impossible: sqrt(D) rational"
        if p > 0:                      # q < 0
            return 1 if t > 0 else -1
        return -1 if t > 0 else 1      # p < 0, q > 0

    def __repr__(self):
        if self.q == 0:
            return str(self.p)
        if self.p == 0:
            return "(%s)*sqrt(%d)" % (self.q, self.D)
        return "%s + (%s)*sqrt(%d)" % (self.p, self.q, self.D)


def squarefree_split(n):
    """n = f^2 * D with D squarefree; return (f, D)."""
    assert n > 0
    f, D, m = 1, 1, n
    d = 2
    while d * d <= m:
        e = 0
        while m % d == 0:
            m //= d
            e += 1
        f *= d ** (e // 2)
        if e % 2:
            D *= d
        d += 1
    D *= m
    return f, D


