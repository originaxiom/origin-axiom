    zero, one = QF(0, 0, D), QF(1, 0, D)

    freqs_by_n = {}
    all_pq = []
    ranks_cum = []
    complexity = []
    emp_max_dev = 0.0
    # float sqrt used ONLY for the DIAGNOSTIC empirical comparison; never verdict-bearing
    sqrtD = math.sqrt(D)

    for n in range(1, N_MAX + 1):
        Ln = langs[n]
