MAT = {"R": ((1, 1), (0, 1)), "L": ((1, 0), (1, 1))}   # repo convention (B139 probe.py)
RHO = {"R": {"a": "a", "b": "ab"}, "L": {"a": "ba", "b": "b"}}  # abelianizations R, L


def mat_mul(A, B):
    return tuple(tuple(sum(A[i][t] * B[t][j] for t in range(2)) for j in range(2))
                 for i in range(2))


def word_matrix(word):
    M = ((1, 0), (0, 1))
    for ch in word:
        M = mat_mul(M, MAT[ch])
    return M


def apply_sub(sub, w):
    return "".join(sub[c] for c in w)


def compose(word):
    """sigma_W = rho_{w1} o rho_{w2} o ... o rho_{wk} (rightmost applied first)."""
    sub = {"a": "a", "b": "b"}
    for ch in word:  # left to right accumulation of the OUTER composition
        r = RHO[ch]
        sub = {c: apply_sub(sub, r[c]) for c in "ab"}
    return sub


def abelianization(sub):
    return tuple(tuple(sub[c].count(l) for c in "ab") for l in "ab")


def is_primitive_word(w):
    k = len(w)
    for d in range(1, k):
        if k % d == 0 and w == w[:d] * (k // d):
            return False
    return True


def necklace_reps(k):
    reps = set()
    for bits in itertools.product("LR", repeat=k):
        w = "".join(bits)
        if "R" not in w or "L" not in w or not is_primitive_word(w):
            continue
        reps.add(min(w[i:] + w[:i] for i in range(k)))
    return sorted(reps)


