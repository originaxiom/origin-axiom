        Mn = nblock_matrix(sub, Ln, n)
        assert int_matrix_primitive(Mn), "n-block matrix not primitive"
        A = [[QF(Mn[i][j], 0, D) - (lam if i == j else zero)
              for j in range(len(Ln))] for i in range(len(Ln))]
        basis = nullspace_QF(A, D)
        assert len(basis) == 1, "Perron eigenspace not 1-dimensional (nullity=%d)" % len(basis)
        v = basis[0]
        s = zero
        for x in v:
            s = s + x
        assert not s.is_zero()
        freq = [x / s for x in v]
        # strict positivity => (Perron-Frobenius) lambda IS the Perron root of Mn
        for x in freq:
            assert x.sign() == 1, "eigenvector not strictly positive"
        # sum check (exact, by construction but asserted)
