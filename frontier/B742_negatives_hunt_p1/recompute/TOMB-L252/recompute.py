def languages_and_longword(sub, n_max, min_len=20000, max_len=2_000_000):
    """Return ({n: sorted list of n-factors}, long deterministic word).
    For a primitive substitution the language of the subshift equals the increasing
    union over m of the factor sets of sigma^m('a')."""
    unions = {n: set() for n in range(1, n_max + 1)}
    w = "a"
    stable = 0
    for _ in range(200):
        w = apply_sub(sub, w)
        changed = False
        for n in range(1, n_max + 1):
            for i in range(len(w) - n + 1):
                f = w[i:i + n]
                if f not in unions[n]:
                    unions[n].add(f)
                    changed = True
        if not changed and len(w) >= min_len:
            stable += 1
        else:
            stable = 0
        if stable >= 2 or len(w) > max_len:
            break
    return {n: sorted(unions[n]) for n in unions}, w


