def nblock_matrix(sub, Ln, n):
    idx = {w: i for i, w in enumerate(Ln)}
    m = len(Ln)
    M = [[0] * m for _ in range(m)]
    for j, w in enumerate(Ln):
        s = apply_sub(sub, w)
        p = len(sub[w[0]])
        assert len(s) >= p - 1 + n
        for t in range(p):
            blk = s[t:t + n]
            assert blk in idx, "n-block image outside language: %r -> %r" % (w, blk)
            M[idx[blk]][j] += 1
    return M


def int_matrix_primitive(M):
    m = len(M)
    A = [row[:] for row in M]
    for _ in range(max(1, m * m)):
        if all(A[i][j] > 0 for i in range(m) for j in range(m)):
            return True
        A = [[sum(A[i][t] * M[t][j] for t in range(m)) for j in range(m)]
             for i in range(m)]
    return all(A[i][j] > 0 for i in range(m) for j in range(m))


def nullspace_QF(A, D):
    """Exact nullspace basis of square QF-matrix A over Q(sqrt(D))."""
    m = len(A)
    B = [row[:] for row in A]
    piv_cols, r = [], 0
    for c in range(m):
        piv = None
        for i in range(r, m):
            if not B[i][c].is_zero():
                piv = i
                break
        if piv is None:
            continue
        B[r], B[piv] = B[piv], B[r]
        inv = B[r][c].inv()
        B[r] = [x * inv for x in B[r]]
        for i in range(m):
            if i != r and not B[i][c].is_zero():
                f = B[i][c]
                B[i] = [x - f * y for x, y in zip(B[i], B[r])]
        piv_cols.append(c)
        r += 1
    free = [c for c in range(m) if c not in piv_cols]
    basis = []
    for fc in free:
        v = [QF(0, 0, D) for _ in range(m)]
        v[fc] = QF(1, 0, D)
        for i, pc in enumerate(piv_cols):
            v[pc] = -B[i][fc]
        basis.append(v)
    return basis


def rank_Q(vecs):
    """Rank over Q of vectors (p, q) in Q^2 (coordinates in basis {1, sqrt(D)})."""
    rows = [list(v) for v in vecs]
    r = 0
    for col in range(2):
        piv = None
        for i in range(r, len(rows)):
            if rows[i][col] != 0:
                piv = i
                break
        if piv is None:
            continue
        rows[r], rows[piv] = rows[piv], rows[r]
        pr = rows[r]
        for i in range(len(rows)):
            if i != r and rows[i][col] != 0:
                f = Fraction(rows[i][col], 1) / pr[col]
                rows[i] = [a - f * b for a, b in zip(rows[i], pr)]
        r += 1
    return r


