def gauss_sum(entry, r):
    Mf, box = entry["Mf"], entry["box"]
    total = mp.mpc(0, 0)
    for mu in box:
        if mu[0] == 0 and mu[1] == 0:
            q = Fraction(0)
        else:
            q = Fraction(0)
            for i in range(2):
                if mu[i]:
                    for j in range(2):
                        if mu[j]:
                            q += mu[i] * Mf[i][j] * mu[j]
        e = (r * q) % 2
        angle = mp.pi * mp.mpf(e.numerator) / mp.mpf(e.denominator) if e != 0 else mp.mpf(0)
        total += mp.mpc(mp.cos(angle), -mp.sin(angle))     # exp(-i pi e)
    return total


