def hnf_column_diag(B):
    """Column-HNF of B (sympy), diagonal entries (abs) -- the fundamental box
    shape, exactly the E6 template's convention."""
    M = sp.Matrix(B.tolist())
    H = hermite_normal_form(M)
    diag = [abs(int(H[i, i])) for i in range(2)]
    return diag


def validate_transversal(B, box_points):
    """Independent correctness check (this is a fresh port, not blind trust):
    every pair of box points must be inequivalent mod the B*Z^2 lattice,
    i.e. B^{-1}(mu_i - mu_j) must never be an integer vector for i != j."""
    Bs = sp.Matrix(B.tolist())
    Binv = Bs.inv()
    n = len(box_points)
    for i in range(n):
        for j in range(i + 1, n):
            diff = sp.Matrix([box_points[i][k] - box_points[j][k] for k in range(2)])
            v = Binv * diff
            if all(x == sp.floor(x) for x in v):
                return False, (i, j)
    return True, None


