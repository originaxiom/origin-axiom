def build_weyl_group():
    gens = [s1, s2]
    I = np.eye(2, dtype=np.int64)
    seen = {I.tobytes()}
    mats = [I]
    frontier = [I]
    while frontier:
        new = []
        for M in frontier:
            for g in gens:
                Mg = g @ M
                key = Mg.tobytes()
                if key not in seen:
                    seen.add(key)
                    new.append(Mg)
                    mats.append(Mg)
        frontier = new
    return mats


