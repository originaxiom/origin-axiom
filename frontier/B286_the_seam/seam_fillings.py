"""B286 -- THE SEAM: the figure-eight is an OPEN object (a complement), and its symmetry-breaking, sign, scale, and
clock all live where it CLOSES against 'the nothing' (the cusp / Dehn filling). Run with sage-python (SnapPy).

Verifies (against the meditation that the ingredients sit at the boundary, not in the bulk):
  1. EXACTLY 10 exceptional Dehn fillings: integer slopes {-4..4} + infinity -- a forced, finite selection set.
  2. CLOSING BREAKS AMPHICHIRALITY: cusped m004 has CS=0 (amphichiral); every generic filling is CHIRAL
     (CS not in {0,1/2} mod 1), and the mirror slope (p,-q) carries CS=-CS -- the ORIENTED closing picks the sign.
  3. SCALE from the closing: the core geodesic length ~ 2*pi/n -> 0 along (1,n) -- a hierarchy generated by filling.
  4. The CLOCK is peripheral: H1(cusp T^2)=Z^2, <mu,lambda>=1 symplectic; a filling = a Lagrangian/polarization.
"""
import snappy
from math import gcd, pi


def exceptional_integer_slopes():
    M = snappy.Manifold('m004'); exc = []
    for n in range(-7, 8):
        N = M.copy(); N.dehn_fill((n, 1))
        hyp = (N.solution_type() == 'all tetrahedra positively oriented' and float(N.volume()) > 0.3)
        if not hyp: exc.append(n)
    return exc                                    # expect [-4,-3,-2,-1,0,1,2,3,4]; + infinity = 10 total


def filling_cs(p, q):
    M = snappy.Manifold('m004'); _ = float(M.chern_simons())     # seed CS on the (amphichiral) cusped object
    N = M.copy(); N.dehn_fill((p, q))
    if N.solution_type() != 'all tetrahedra positively oriented': return None
    return float(N.chern_simons())


def is_chiral(cs):
    c = cs % 1.0
    return min(c, abs(c - 0.5), abs(c - 1.0)) > 1e-4              # amphichiral <=> CS in {0,1/2} mod 1


def core_length(p, q):
    M = snappy.Manifold('m004'); N = M.copy(); N.dehn_fill((p, q))
    cl = N.cusp_info(0).get('core_length')
    return float(abs(cl)) if cl is not None else None


if __name__ == "__main__":
    M = snappy.Manifold('m004')
    print("cusped m004: vol =", float(M.volume()), " CS =", float(M.chern_simons()), "(=0, amphichiral)")
    exc = exceptional_integer_slopes()
    print("\n1. exceptional integer slopes:", exc, "+ infinity  => count", len(exc) + 1)
    assert exc == [-4, -3, -2, -1, 0, 1, 2, 3, 4] and len(exc) + 1 == 10

    print("\n2. closing breaks amphichirality (CS sign = chosen handedness):")
    for (p, q) in [(5, 1), (7, 1), (5, 2), (7, 2), (8, 3), (9, 2)]:
        c, cm = filling_cs(p, q), filling_cs(p, -q)
        flip = min(((c + cm) % 1.0), 1.0 - ((c + cm) % 1.0)) < 1e-4
        print(f"   ({p},{q}): CS={c:+.6f}  CS(mirror)={cm:+.6f}  CS(mirror)=-CS:{flip}  chiral:{is_chiral(c)}")
        assert is_chiral(c) and flip

    print("\n3. scale hierarchy: core geodesic length along (1,n) ~ 2*pi/n:")
    for n in [5, 10, 20, 50, 100]:
        L = core_length(1, n); print(f"   (1,{n}): core={L:.6e}   2pi/n={2*pi/n:.6e}   ratio={L/(2*pi/n):.4f}")

