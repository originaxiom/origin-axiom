def _alg_dim_num(mats, n, tol=1e-7):
    basis, frontier = [np.eye(n, dtype=complex)], [np.eye(n, dtype=complex)]
    for _ in range(8):
        nf = []
        for M in frontier:
            for g in mats:
                nf.append(M @ g)
        cand = basis + nf
        flat = np.array([m.flatten() for m in cand])
        s = np.linalg.svd(flat, compute_uv=False)
        rank = int((s > tol * max(1.0, s[0])).sum())
        frontier = nf
        if rank == len(basis):                     # stabilised
            return rank
        basis = cand
        if rank == n * n:
            return n * n
    flat = np.array([m.flatten() for m in basis])
    s = np.linalg.svd(flat, compute_uv=False)
    return int((s > tol * max(1.0, s[0])).sum())


def phi_fixed_search_sl3(starts=60, seed=20260609, A_eigs=(1.0, -1.0, -1.0)):
    rng = np.random.default_rng(seed)
    A = np.diag(np.array(A_eigs, dtype=complex))
    n = 3

    def resid(v):
        g = (v[:9] + 1j * v[9:]).reshape(3, 3)
        try:
            gi = np.linalg.inv(g)
        except np.linalg.LinAlgError:
            return np.ones(18) * 1e3
        M = g @ A @ gi - A @ gi @ A @ g
        return np.concatenate([M.real.flatten(), M.imag.flatten()])

    from scipy.optimize import least_squares
    irreducible = 0
    converged = 0
    for _ in range(starts):
        v0 = rng.standard_normal(18)
        sol = least_squares(resid, v0, method="lm", max_nfev=400)
        if sol.cost > 1e-12:
            continue
        g = (sol.x[:9] + 1j * sol.x[9:]).reshape(3, 3)
        if abs(np.linalg.det(g)) < 1e-6:
            continue
        converged += 1
        B = np.linalg.inv(g) @ A @ g
        if _alg_dim_num([A, B], n) == 9:
            irreducible += 1
    return {"A_eigs": A_eigs, "starts": starts, "converged": converged, "irreducible_found": irreducible,
            "all_reducible": irreducible == 0}


