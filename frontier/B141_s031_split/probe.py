# ----------------------------------------------------------------------------------------------------------------
# algebra dimension (exact, sympy): dim of the matrix algebra generated by `gens` (BFS over words, rank-capped n^2).
# ----------------------------------------------------------------------------------------------------------------
def algebra_dim_exact(gens, n, max_depth=8):
    mats = [sp.eye(n)]
    frontier = [sp.eye(n)]
    dim = 1
    for _ in range(max_depth):
        nf = []
        for M in frontier:
            for g in gens:
                nf.append(sp.expand(M * g))
        mats.extend(nf)
        frontier = nf
        dim = sp.Matrix([list(m) for m in mats]).rank()
        if dim == n * n:
            break
    return int(dim)


# ----------------------------------------------------------------------------------------------------------------
# ITEM 1 -- the Q8 phi-fixed rep and its reducible Sym tower.
# ----------------------------------------------------------------------------------------------------------------
def q8_rep():
    A = sp.Matrix([[I, 0], [0, -I]])
    B = sp.Matrix([[0, 1], [-1, 0]])
    return A, B


def q8_relations():
    A, B = q8_rep()
    return {"A2_eq_-I": A * A == -sp.eye(2), "B2_eq_-I": B * B == -sp.eye(2),
            "AB_eq_-BA": A * B == -(B * A), "detA": A.det(), "detB": B.det()}


def q8_group_order():
    """The group generated by A,B (order should be 8 = Q8)."""
    A, B = q8_rep()
    seen, frontier = {sp.ImmutableMatrix(sp.eye(2))}, [sp.eye(2)]
    for _ in range(10):
        nf = []
        for M in frontier:
            for g in (A, B):
                P = sp.ImmutableMatrix(sp.expand(M * g))
                if P not in seen:
                    seen.add(P); nf.append(P)
        frontier = nf
        if not nf:
            break
    return len(seen)


